package com.example.example.menu;

public class PropertyMenuSelect {

    private String name1=null;
    private String name2=null;
    private String name3=null;
    private String name4=null;
    private String name5=null;

    public PropertyMenuSelect(String name1, String name2, String name3, String name4, String name5) {
        this.name1 = name1;
        this.name2 = name2;
        this.name3 = name3;
        this.name4 = name4;
        this.name5 = name5;
    }

    public String getName1() {
        return name1;
    }

    public String getName2() {
        return name2;
    }

    public String getName3() {
        return name3;
    }

    public String getName4() {
        return name4;
    }

    public String getName5() {
        return name5;
    }
}
